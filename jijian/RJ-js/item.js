$(function () {
  $('#showDialog1').click(function() {
    $('#dialog1').show()
  })

  $('#showDialog2').click(function() {
    $('#dialog2').show()
  })

  $('#cloasDialog1').click(function() {
    $('#dialog1').hide()
  })

  $('#cloasDialog2').click(function() {
    $('#dialog2').hide()
  })
})
